# Pro experimentování s parametry příkazové řádky je 
# nejlepší  nejdřív vybuildovat binární program pomocí 
# `go build`.
$ go build command-line-arguments.go
$ ./command-line-arguments a b c d
[./command-line-arguments a b c d]       
[a b c d]
c

